package Day3Package;

public class LogicalOperators {
    public static void main(String[] args){
        int a=2;
        int b=4;
        System.out.println("And LogicalOperators");//if one is false so it will give answer false
        System.out.println(a>b && a<b);
        System.out.println("Or LogicalOperators");//if one is true so it will give answer true
        System.out.println(a>b || a<b);
    }
}
